    function GetData() {
        var ArrData = [];
        for (var option of document.getElementById("jasa").options) {
            ArrData.push(option.value);
            SimpanToDB(option.value);
        }
        console.log(ArrData);
    }

    function SimpanToDB(nama_jasa){
        var myHeaders = new Headers();
        myHeaders.append("Content-Type", "application/x-www-form-urlencoded");

        var urlencoded = new URLSearchParams();
        urlencoded.append("nama_jasa", nama_jasa);
        var requestOptions = {
        method: 'POST',
        headers: myHeaders,
        body: urlencoded,
        redirect: 'follow'
        };

        fetch("<?=base_url()?>api/jasa", requestOptions)
        .then(response => response.json())
        .then(result => console.log(result))
        .catch(error => console.log('error', error));
    }
</script>

